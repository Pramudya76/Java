public class Matematika {

    void Pertambahan(float ValueA, float ValueB) {
        float hasil = ValueA + ValueB;
        System.out.println("Pertambahan : " + ValueA + " + " + ValueB + " : " + hasil);
    }

    void Pengurangan(float ValueA, float ValueB) {
        float hasil = ValueA - ValueB;
        System.out.println("Pengurangan : " + ValueA + " - " + ValueB + " : " + hasil);
    }

    void Perkalian(float ValueA, float ValueB) {
        float hasil = ValueA * ValueB;
        System.out.println("Perkalian : " + ValueA + " * " + ValueB + " : " + hasil);
    }

    void Pembagian(float ValueA, float ValueB) {
        float hasil = ValueA / ValueB;
        System.out.println("Pembagian : " + ValueA + " / " + ValueB + " : " + hasil);
    }

}
